public class LacoRepeticao {
    public static void main(String[] args) {
        
        //WHILE
        int numero = 0;
        
        
        while (numero<10){
            numero += 1;
            System.out.println(numero);
        }

        //FOR

        int incrementa = 0;

        for (int cont = 0; cont < 10; cont ++){
            incrementa ++;
            System.out.println(incrementa);
        }


    }
}
